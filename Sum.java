// Program for Sum

//addition of two numbers

import java.util.Scanner;

class Sum{
public static void main(String args[])
{
int a,b, sum=0;
Scanner s = new Scanner(System.in);
a=s.nextInt();
b=s.nextInt();
sum = a+b;
System.out.println("Sum is : "+sum);
}
}

//Output is = let a=5, b=7, sum = 12